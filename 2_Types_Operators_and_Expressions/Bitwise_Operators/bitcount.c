/* bitcount: count 1 bits in x */
int bitcount(unsigned x) {
  int b = 0;

  for (b = 0; x != 0; x >>= 1)
    if (x & 01)
      b++;
  return b;
}

/* bitcount2: count 1 bits in x faster */
int bitcount2(unsigned x) {
  int b = 0;

  while (x) {
    x &= (x - 1);
    b++;
  }

  return b;
}

